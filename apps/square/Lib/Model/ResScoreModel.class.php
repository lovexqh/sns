class ResScoreModel extends Model{
  var $tableName	=	'resource_score';

/**
 +----------------------------------------------------------
 * 显示资源所对应的积分
 +----------------------------------------------------------
 * @param	$id 资源id
 * @return	int 资源得分
 * @author	美美2013-3-5
 +----------------------------------------------------------
 * 创建时间：	2013-3-5 上午07:18:31
 +----------------------------------------------------------
 */
  public function getScore($id){
	  
	 $score=D('ResScore')->where("id=$id")->avg('score');
	 $score=intval($score);
	 return $score; 
  }
}
