class DatadictionaryModel extends Model{
  var $tableName	=	'edu_datadictionary';
  /**
   +----------------------------------------------------------
   * 获取版本信息
   +----------------------------------------------------------
   * @return	Array 版本信息
   * @author	美美2013-3-4
   +----------------------------------------------------------
   * 创建时间：	2013-3-4 上午02:25:00
   +----------------------------------------------------------
   */
  public function getPublisher() {
    	return $this->where("`DataType`='Publisher'")->findall();
  }
   /**
    +----------------------------------------------------------
    * 获取班级信息
    +----------------------------------------------------------
    * @return	Array 获取班级信息
    * @author	美美2013-3-4
    +----------------------------------------------------------
    * 创建时间：	2013-3-4 上午02:25:32
    +----------------------------------------------------------
    */
  public function getDictionary() {
    	return $this->where("`DataType`='Grade'")->findall();
  }
  
  /**
   +----------------------------------------------------------
   * ajax获取当前选中的年级的课程
   +----------------------------------------------------------
   * @param	 $grade 年级
   * @return	Array 当前选中的年级的课程信息
   * @author	美美2013-3-4
   +----------------------------------------------------------
   * 创建时间：	2013-3-4 上午02:26:03
   +----------------------------------------------------------
   */
  public function getDictionary($grade) {
    	return $this->where("DataType='$grade'")->findall();
  }
}
